    private void jCboProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCboProdutosActionPerformed
        JlrProdutos jlrProdutos = (JlrProdutos) jCboProdutos.getSelectedItem();
        jTxtValorUni.setText(Util.doubleToStr(jlrProdutos.getJlrPreco()));
        int quant = Util.strToInt(jTxtQuantidade.getText());
        jTxtTotal.setText(Util.doubleToStr( quant * jlrProdutos.getJlrPreco()));
    }//GEN-LAST:event_jCboProdutosActionPerformed

    private void jTxtQuantidadeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtQuantidadeKeyReleased
        if(jTxtQuantidade.getText().isEmpty() == false){
            JlrProdutos jlrProdutos = (JlrProdutos) jCboProdutos.getSelectedItem();
            int quant = Util.strToInt(jTxtQuantidade.getText());
            jTxtTotal.setText(Util.doubleToStr( quant * jlrProdutos.getJlrPreco()));
        } else {
            Util.limpar(jTxtTotal);
        }
    }//GEN-LAST:event_jTxtQuantidadeKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JlrVendasprodutos jlrVendasProdutos = new JlrVendasprodutos();
        jlrVendasProdutos.setJlrProdutos((JlrProdutos) jCboProdutos.getSelectedItem());
        jlrVendasProdutos.setJlrQuantidade(Util.strToInt(jTxtQuantidade.getText()) );
        jlrVendasProdutos.setJlrValorUnitario(Util.strToDouble(jTxtValorUni.getText()) );
        jDlgVendas.controllerVendasProd.addBean(jlrVendasProdutos);
        if (jDlgVendas != null) {
            jDlgVendas.Total();
        }
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

